<?php
echo "Hello Today I learn Switch case ";
echo "<br>";
$age = 21;

switch($age){
    case 15:
        echo "You are 15 year old <br>";
        break;
    case 16:
        echo "You are 16 year old <br>";
        break;
    case 17:
        echo "You are 17 year old <br>";
        break;
    case 20:
        echo "You are 20 year old <br>";
        break;
    case 21:
        echo "You are 21 year old <br>";
        break;
    default:
        echo "Your age is Weird <br>";
        break;
}
?>